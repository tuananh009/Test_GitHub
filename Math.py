#Thư viện Math trong python
import math
x=8/3
print(x)
#Lấy phần nguyên của số thập phân
print(math.trunc(x))
#Làm tròn thành số nhỏ hơn
print(math.floor(x))
#Làm tròn thành số lớn hơn
print(math.ceil(x))
#Trả về số thực trong trị tuyệt đối
print(math.fabs(-x))
#Trả về số thực là căn bậc 2
print(math.sqrt(x))
#Trả về số nguyên là ước chung lớn nhất của x và y

print(math.gcd(10,6))
